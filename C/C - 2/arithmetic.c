// Program to explain arithmetic operators

#include<stdio.h>
#include<conio.h>

void main()
{
    int a=10, b=15;

    printf(" the value of a + b will be %d\n", a+b);
    printf(" the value of a * b will be %d\n", a*b);
    printf(" the value of a - b will be %d\n", a-b);
    printf(" the value of a / b will be %d\n", a/b);
    printf(" the value of b %% a will be %d\n", b%a);
}